/// Prost annotation type tag for a field (e.g. "int32", "string", "message", "enumeration=...").
fn prost_field_tag(field: &FieldDescriptorProto, package: &str) -> String {
    let field_type = field.r#type.unwrap_or(FieldType::Int32);
    match field_type {
        FieldType::Enum => {
            let type_name = field.type_name.as_deref().unwrap_or("");
            let rust_path = fqn_to_rust_path(type_name, package);
            format!("enumeration = \"{rust_path}\"")
        }
        FieldType::Message => "message".to_string(),
        _ => prost_type_tag(field_type).to_string(),
    }
}

/// Prost type tag string for a scalar type.
fn prost_type_tag(ft: FieldType) -> &'static str {
    match ft {
        FieldType::Double => "double",
        FieldType::Float => "float",
        FieldType::Int64 => "int64",
        FieldType::Uint64 => "uint64",
        FieldType::Int32 => "int32",
        FieldType::Fixed64 => "fixed64",
        FieldType::Fixed32 => "fixed32",
        FieldType::Bool => "bool",
        FieldType::String => "string",
        FieldType::Bytes => "bytes = \"vec\"",
        FieldType::Uint32 => "uint32",
        FieldType::Sfixed32 => "sfixed32",
        FieldType::Sfixed64 => "sfixed64",
        FieldType::Sint32 => "sint32",
        FieldType::Sint64 => "sint64",
        FieldType::Enum => "int32", // fallback
        FieldType::Message | FieldType::Group => "message",
    }
}

fn prost_type_tag_for_map_value(field: &FieldDescriptorProto, package: &str) -> String {
    let ft = field.r#type.unwrap_or(FieldType::Int32);
    match ft {
        FieldType::Enum => {
            let type_name = field.type_name.as_deref().unwrap_or("");
            let rust_path = fqn_to_rust_path(type_name, package);
            format!("enumeration({rust_path})")
        }
        _ => prost_type_tag(ft).to_string(),
    }
}

/// Rust type for a field.
fn rust_field_type(field: &FieldDescriptorProto, package: &str) -> String {
    let ft = field.r#type.unwrap_or(FieldType::Int32);
    match ft {
        FieldType::Message => {
            let type_name = field.type_name.as_deref().unwrap_or("Unknown");
            fqn_to_rust_path(type_name, package)
        }
        FieldType::Enum => "i32".to_string(),
        _ => rust_type(ft).to_string(),
    }
}

/// Rust type for a map value field.
fn rust_map_value_type(field: &FieldDescriptorProto, package: &str) -> String {
    let ft = field.r#type.unwrap_or(FieldType::Int32);
    match ft {
        FieldType::Message => {
            let type_name = field.type_name.as_deref().unwrap_or("Unknown");
            fqn_to_rust_path(type_name, package)
        }
        FieldType::Enum => "i32".to_string(),
        _ => rust_type(ft).to_string(),
    }
}

/// Rust type for a scalar FieldType.
fn rust_type(ft: FieldType) -> &'static str {
    match ft {
        FieldType::Double => "f64",
        FieldType::Float => "f32",
        FieldType::Int64 | FieldType::Sfixed64 | FieldType::Sint64 => "i64",
        FieldType::Uint64 | FieldType::Fixed64 => "u64",
        FieldType::Int32 | FieldType::Sfixed32 | FieldType::Sint32 | FieldType::Enum => "i32",
        FieldType::Uint32 | FieldType::Fixed32 => "u32",
        FieldType::Bool => "bool",
        FieldType::String => "::prost::alloc::string::String",
        FieldType::Bytes => "::prost::alloc::vec::Vec<u8>",
        FieldType::Message | FieldType::Group => "Unknown",
    }
}

// ---------------------------------------------------------------------------
// Naming utilities
// ---------------------------------------------------------------------------

/// Convert to snake_case, handling Rust keywords.
fn to_snake(name: &str) -> String {
    let s = name.to_snake_case();
    sanitize_keyword(&s)
}

/// Convert to UpperCamelCase.
fn to_upper_camel(name: &str) -> String {
    let s = name.to_upper_camel_case();
    if s.is_empty() {
        "Unknown".to_string()
    } else {
        s
    }
}

/// Sanitize Rust keywords in field names.
/// Uses trailing underscore for `self`/`super` (cannot use `r#` for these),
/// and `r#` prefix for all other keywords (matching prost behavior).
fn sanitize_keyword(name: &str) -> String {
    match name {
        // self/super cannot use r# syntax in all positions
        "self" | "super" => format!("{name}_"),
        "as" | "break" | "const" | "continue" | "crate" | "else" | "enum" | "extern" | "false"
        | "fn" | "for" | "if" | "impl" | "in" | "let" | "loop" | "match" | "mod" | "move"
        | "mut" | "pub" | "ref" | "return" | "Self" | "static" | "struct"
        | "trait" | "true" | "type" | "unsafe" | "use" | "where" | "while" | "async"
        | "await" | "dyn" | "abstract" | "become" | "box" | "do" | "final" | "macro"
        | "override" | "priv" | "typeof" | "unsized" | "virtual" | "yield" | "try" => {
            format!("r#{name}")
        }
        _ => name.to_string(),
    }
}

/// Strip the common enum prefix from a value name.
///
/// E.g., strip_enum_prefix("Status", "STATUS_ACTIVE") -> "ACTIVE"
/// E.g., strip_enum_prefix("Color", "RED") -> "RED" (no prefix match)
fn strip_enum_prefix(enum_name: &str, value_name: &str) -> String {
    let prefix = enum_name.to_snake_case().to_uppercase();
    let stripped = if value_name.starts_with(&prefix) {
        let rest = &value_name[prefix.len()..];
        if rest.starts_with('_') {
            &rest[1..]
        } else {
            value_name
        }
    } else {
        value_name
    };
    if stripped.is_empty() {
        value_name.to_string()
    } else {
        stripped.to_string()
    }
}

/// Convert a fully-qualified proto type name to a Rust module path.
///
/// Strips the current package prefix to produce relative paths (matching prost behavior).
/// E.g. with package "example": ".example.Outer.Inner" -> "outer::Inner"
fn fqn_to_rust_path(fqn: &str, current_package: &str) -> String {
    let stripped = fqn.strip_prefix('.').unwrap_or(fqn);

    // Strip the current package prefix to produce relative paths
    let relative = if !current_package.is_empty() && stripped.starts_with(current_package) {
        let rest = &stripped[current_package.len()..];
        if rest.starts_with('.') {
            &rest[1..]
        } else if rest.is_empty() {
            rest
        } else {
            stripped // Package name is a prefix of a longer name, don't strip
        }
    } else {
        stripped
    };

    let parts: Vec<&str> = relative.split('.').collect();
    if parts.is_empty() || (parts.len() == 1 && parts[0].is_empty()) {
        return "Unknown".to_string();
    }

    // Last component is the type name (UpperCamel), everything before is package/nesting
    let mut rust_parts = Vec::new();
    for (i, part) in parts.iter().enumerate() {
        if i == parts.len() - 1 {
            rust_parts.push(to_upper_camel(part));
        } else {
            rust_parts.push(to_snake(part));
        }
    }

    if rust_parts.len() == 1 {
        return rust_parts[0].clone();
    }

    rust_parts.join("::")
}

