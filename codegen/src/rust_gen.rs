//! Prost-compatible Rust code generator.
//!
//! Generates Rust structs and enums from a `FileDescriptorSet` that are compatible
//! with `prost`'s derive macros (`::prost::Message`, `::prost::Enumeration`, `::prost::Oneof`).

use std::collections::HashMap;

use heck::{ToSnakeCase, ToUpperCamelCase};
use protoc_rs_schema::*;

/// Generate Rust code for all files in a `FileDescriptorSet`.
///
/// Returns a map from module path (e.g. `"my.package"`) to generated Rust source code.
/// Files without a package use an empty string key.
pub fn generate_rust(
    fds: &FileDescriptorSet,
) -> Result<HashMap<String, String>, CodeGenError> {
    let mut result = HashMap::new();
    for file in &fds.file {
        let package = file.package.as_deref().unwrap_or("");
        let filename = if package.is_empty() {
            // Use the proto filename (sans .proto) as the module name
            file.name
                .as_deref()
                .unwrap_or("unknown")
                .trim_end_matches(".proto")
                .replace('/', ".")
                .to_string()
        } else {
            package.to_string()
        };
        let source = generate_file(file)?;
        if !source.trim().is_empty() {
            result.insert(format!("{filename}.rs"), source);
        }
    }
    Ok(result)
}

/// Generate Rust code for a single file.
fn generate_file(file: &FileDescriptorProto) -> Result<String, CodeGenError> {
    let syntax = file.syntax_enum();
    let package = file.package.as_deref().unwrap_or("");
    let mut gen = RustGen {
        buf: String::new(),
        indent: 0,
        syntax: syntax.clone(),
        package: package.to_string(),
        nesting: Vec::new(),
    };

    gen.push_line("// This file is @generated by protoc-rs.");

    // Messages first (matches prost ordering)
    for msg in &file.message_type {
        gen.gen_message(msg)?;
        gen.push_line("");
    }

    // Top-level enums
    for e in &file.enum_type {
        gen.gen_enum(e)?;
        gen.push_line("");
    }

    Ok(gen.buf)
}

#[derive(Debug)]
pub enum CodeGenError {
    MissingFieldName,
    MissingMessageName,
    MissingEnumName,
}

impl std::fmt::Display for CodeGenError {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        match self {
            Self::MissingFieldName => write!(f, "missing field name in message"),
            Self::MissingMessageName => write!(f, "missing message name"),
            Self::MissingEnumName => write!(f, "missing enum name"),
        }
    }
}

impl std::error::Error for CodeGenError {}

// ---------------------------------------------------------------------------
// Generator state
// ---------------------------------------------------------------------------

struct RustGen {
    buf: String,
    indent: u32,
    syntax: Syntax,
    package: String,
    /// Current nesting path of message names (e.g., ["Outer", "Inner"]).
    /// Used to produce relative type paths for nested references.
    nesting: Vec<String>,
}

impl RustGen {
    fn push_indent(&mut self) {
        for _ in 0..self.indent {
            self.buf.push_str("    ");
        }
    }

    fn push_line(&mut self, line: &str) {
        if line.is_empty() {
            self.buf.push('\n');
        } else {
            self.push_indent();
            self.buf.push_str(line);
            self.buf.push('\n');
        }
    }

    // -----------------------------------------------------------------------
    // Message generation
    // -----------------------------------------------------------------------

    /// Build the fully-qualified prefix for the current scope (package + nesting).
    /// E.g., with package "test" and nesting ["Outer"], returns "test.Outer".
    fn current_fqn_prefix(&self) -> String {
        let mut parts = Vec::new();
        if !self.package.is_empty() {
            parts.push(self.package.clone());
        }
        parts.extend(self.nesting.iter().cloned());
        parts.join(".")
    }

    fn gen_message(&mut self, msg: &DescriptorProto) -> Result<(), CodeGenError> {
        let name = msg
            .name
            .as_deref()
            .ok_or(CodeGenError::MissingMessageName)?;
        let rust_name = to_upper_camel(name);

        // Skip map entry types -- they are inlined into the parent field
        if is_map_entry(msg) {
            return Ok(());
        }

        // Partition fields: regular vs oneof members
        let (regular_fields, oneof_groups) = partition_fields(msg);

        // Emit struct
        self.push_line("#[derive(Clone, PartialEq, ::prost::Message)]");
        self.push_line(&format!("pub struct {rust_name} {{"));
        self.indent += 1;

        // Regular fields
        for field in &regular_fields {
            self.gen_field(field, msg)?;
        }

        // Oneof fields
        let mod_name = to_snake(name);
        for (oneof_idx, fields) in &oneof_groups {
            let oneof_decl = &msg.oneof_decl[*oneof_idx as usize];
            let oneof_name = oneof_decl.name.as_deref().unwrap_or("unknown");
            let oneof_rust_name = to_snake(oneof_name);
            let oneof_type_name = to_upper_camel(oneof_name);

            // Collect tags
            let tags: Vec<String> = fields
                .iter()
                .filter_map(|f| f.number.map(|n| format!("{n}")))
                .collect();
            let tags_str = tags.join(", ");

            self.push_line(&format!(
                "#[prost(oneof = \"{mod_name}::{oneof_type_name}\", tags = \"{tags_str}\")]"
            ));
            self.push_line(&format!(
                "pub {oneof_rust_name}: ::core::option::Option<{mod_name}::{oneof_type_name}>,"
            ));
        }

        self.indent -= 1;
        self.push_line("}");

        // Nested mod for nested types, enums, and oneof enums
        let has_nested = !msg.nested_type.iter().all(|m| is_map_entry(m))
            || !msg.enum_type.is_empty()
            || !oneof_groups.is_empty();

        if has_nested {
            // Push nesting for the mod scope -- type refs inside nested
            // messages will be relative to this message's scope.
            self.nesting.push(name.to_string());

            self.push_line(&format!("pub mod {mod_name} {{"));
            self.indent += 1;

            // Nested enums
            for e in &msg.enum_type {
                self.gen_enum(e)?;
                self.push_line("");
            }

            // Nested messages (skip map entries)
            for nested in &msg.nested_type {
                if !is_map_entry(nested) {
                    self.gen_message(nested)?;
                    self.push_line("");
                }
            }

            // Oneof enums
            for (oneof_idx, fields) in &oneof_groups {
                let oneof_decl = &msg.oneof_decl[*oneof_idx as usize];
                self.gen_oneof_enum(oneof_decl, fields, msg)?;
                self.push_line("");
            }

            self.indent -= 1;
            self.push_line("}");

            self.nesting.pop();
        }

        Ok(())
    }

    fn gen_field(
        &mut self,
        field: &FieldDescriptorProto,
        parent_msg: &DescriptorProto,
    ) -> Result<(), CodeGenError> {
        let name = field.name.as_deref().ok_or(CodeGenError::MissingFieldName)?;
        let tag = field.number.unwrap_or(0);
        let rust_name = to_snake(name);
        let label = field.label.unwrap_or(FieldLabel::Optional);

        // Check if this is a map field
        if let Some((key_field, val_field)) = find_map_entry(field, parent_msg) {
            let key_tag = prost_type_tag(key_field.r#type.unwrap_or(FieldType::Int32));
            let val_tag = prost_type_tag_for_map_value(&val_field, &self.current_fqn_prefix());
            self.push_line(&format!(
                "#[prost(map = \"{key_tag}, {val_tag}\", tag = \"{tag}\")]"
            ));
            let key_type = rust_type(key_field.r#type.unwrap_or(FieldType::Int32));
            let val_type = rust_map_value_type(&val_field, &self.current_fqn_prefix());
            self.push_line(&format!(
                "pub {rust_name}: ::std::collections::HashMap<{key_type}, {val_type}>,"
            ));
            return Ok(());
        }

        // Determine wrapping (Option, Vec, or bare)
        let is_repeated = label == FieldLabel::Repeated;
        let is_optional = self.is_field_optional(field);

        // Build prost annotation
        let type_tag = prost_field_tag(field, &self.current_fqn_prefix());
        let is_required = label == FieldLabel::Required;
        let mut prost_parts = vec![type_tag];
        if is_optional {
            prost_parts.push("optional".to_string());
        }
        if is_required {
            prost_parts.push("required".to_string());
        }
        if is_repeated {
            prost_parts.push("repeated".to_string());
            // Proto2 repeated scalar fields are not packed by default
            if matches!(self.syntax, Syntax::Proto2) && is_packable_type(field) {
                prost_parts.push("packed = \"false\"".to_string());
            }
        }
        prost_parts.push(format!("tag = \"{tag}\""));
        let prost_attr = prost_parts.join(", ");

        self.push_line(&format!("#[prost({prost_attr})]"));

        // Build Rust type
        let base_type = rust_field_type(field, &self.current_fqn_prefix());
        let full_type = if is_repeated {
            format!("::prost::alloc::vec::Vec<{base_type}>")
        } else if is_optional {
            format!("::core::option::Option<{base_type}>")
        } else {
            base_type
        };

        self.push_line(&format!("pub {rust_name}: {full_type},"));
        Ok(())
    }

    fn is_field_optional(&self, field: &FieldDescriptorProto) -> bool {
        let label = field.label.unwrap_or(FieldLabel::Optional);
        if label == FieldLabel::Repeated {
            return false;
        }
        if field.proto3_optional == Some(true) {
            return true;
        }
        let field_type = field.r#type.unwrap_or(FieldType::Int32);
        match &self.syntax {
            Syntax::Proto3 => {
                // Proto3: only message fields are optional by default
                field_type == FieldType::Message
            }
            Syntax::Proto2 | Syntax::Unknown(_) => {
                // Proto2: optional fields are Optional, required are bare
                label != FieldLabel::Required
            }
        }
    }

    // -----------------------------------------------------------------------
    // Enum generation
    // -----------------------------------------------------------------------

    fn gen_enum(&mut self, e: &EnumDescriptorProto) -> Result<(), CodeGenError> {
        let name = e.name.as_deref().ok_or(CodeGenError::MissingEnumName)?;
        let rust_name = to_upper_camel(name);

        self.push_line(
            "#[derive(Clone, Copy, Debug, PartialEq, Eq, Hash, PartialOrd, Ord, ::prost::Enumeration)]",
        );
        self.push_line("#[repr(i32)]");
        self.push_line(&format!("pub enum {rust_name} {{"));
        self.indent += 1;

        // Track seen numbers for allow_alias dedup
        let mut seen_numbers = std::collections::HashSet::new();
        for val in &e.value {
            let val_name = val.name.as_deref().unwrap_or("UNKNOWN");
            let number = val.number.unwrap_or(0);
            if !seen_numbers.insert(number) {
                continue; // Skip aliased values
            }
            let stripped = strip_enum_prefix(name, val_name);
            let rust_variant = to_upper_camel(&stripped);
            self.push_line(&format!("{rust_variant} = {number},"));
        }

        self.indent -= 1;
        self.push_line("}");

        // impl as_str_name / from_str_name
        self.push_line(&format!("impl {rust_name} {{"));
        self.indent += 1;

        self.push_line("pub fn as_str_name(&self) -> &'static str {");
        self.indent += 1;
        self.push_line("match self {");
        self.indent += 1;
        seen_numbers.clear();
        for val in &e.value {
            let val_name = val.name.as_deref().unwrap_or("UNKNOWN");
            let number = val.number.unwrap_or(0);
            if !seen_numbers.insert(number) {
                continue;
            }
            let stripped = strip_enum_prefix(name, val_name);
            let rust_variant = to_upper_camel(&stripped);
            self.push_line(&format!(
                "Self::{rust_variant} => \"{val_name}\","
            ));
        }
        self.indent -= 1;
        self.push_line("}");
        self.indent -= 1;
        self.push_line("}");

        self.push_line("pub fn from_str_name(value: &str) -> ::core::option::Option<Self> {");
        self.indent += 1;
        self.push_line("match value {");
        self.indent += 1;
        seen_numbers.clear();
        for val in &e.value {
            let val_name = val.name.as_deref().unwrap_or("UNKNOWN");
            let number = val.number.unwrap_or(0);
            if !seen_numbers.insert(number) {
                continue;
            }
            let stripped = strip_enum_prefix(name, val_name);
            let rust_variant = to_upper_camel(&stripped);
            self.push_line(&format!(
                "\"{val_name}\" => Some(Self::{rust_variant}),"
            ));
        }
        self.push_line("_ => None,");
        self.indent -= 1;
        self.push_line("}");
        self.indent -= 1;
        self.push_line("}");

        self.indent -= 1;
        self.push_line("}");

        Ok(())
    }

    // -----------------------------------------------------------------------
    // Oneof enum generation
    // -----------------------------------------------------------------------

    fn gen_oneof_enum(
        &mut self,
        oneof: &OneofDescriptorProto,
        fields: &[&FieldDescriptorProto],
        _parent_msg: &DescriptorProto,
    ) -> Result<(), CodeGenError> {
        let oneof_name = oneof.name.as_deref().unwrap_or("unknown");
        let rust_name = to_upper_camel(oneof_name);

        self.push_line("#[derive(Clone, PartialEq, ::prost::Oneof)]");
        self.push_line(&format!("pub enum {rust_name} {{"));
        self.indent += 1;

        for field in fields {
            let field_name = field.name.as_deref().unwrap_or("unknown");
            let tag = field.number.unwrap_or(0);
            let type_tag = prost_field_tag(field, &self.current_fqn_prefix());

            self.push_line(&format!("#[prost({type_tag}, tag = \"{tag}\")]"));
            let variant_name = to_upper_camel(field_name);
            let base_type = rust_field_type(field, &self.current_fqn_prefix());

            let field_type = field.r#type.unwrap_or(FieldType::Int32);
            if field_type == FieldType::Message {
                self.push_line(&format!(
                    "{variant_name}(::prost::alloc::boxed::Box<{base_type}>),"
                ));
            } else {
                self.push_line(&format!("{variant_name}({base_type}),"));
            }
        }

        self.indent -= 1;
        self.push_line("}");

        Ok(())
    }
}

// ---------------------------------------------------------------------------
// Field partitioning
// ---------------------------------------------------------------------------

/// Partition fields into regular fields and oneof groups.
/// Proto3 optional fields (with synthetic oneofs) are treated as regular fields.
fn partition_fields<'a>(
    msg: &'a DescriptorProto,
) -> (Vec<&'a FieldDescriptorProto>, Vec<(i32, Vec<&'a FieldDescriptorProto>)>) {
    let mut regular = Vec::new();
    let mut oneof_map: HashMap<i32, Vec<&FieldDescriptorProto>> = HashMap::new();

    for field in &msg.field {
        if field.proto3_optional == Some(true) {
            // Proto3 optional: treat as regular field (not oneof)
            regular.push(field);
        } else if let Some(idx) = field.oneof_index {
            oneof_map.entry(idx).or_default().push(field);
        } else {
            regular.push(field);
        }
    }

    let mut oneof_groups: Vec<(i32, Vec<&FieldDescriptorProto>)> =
        oneof_map.into_iter().collect();
    oneof_groups.sort_by_key(|(idx, _)| *idx);

    (regular, oneof_groups)
}

