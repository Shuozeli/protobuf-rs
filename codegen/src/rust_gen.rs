/// Returns true if the field type supports packed encoding (numeric types, bools, enums).
fn is_packable_type(field: &FieldDescriptorProto) -> bool {
    matches!(
        field.r#type,
        Some(
            FieldType::Double
                | FieldType::Float
                | FieldType::Int64
                | FieldType::Uint64
                | FieldType::Int32
                | FieldType::Fixed64
                | FieldType::Fixed32
                | FieldType::Bool
                | FieldType::Uint32
                | FieldType::Sfixed32
                | FieldType::Sfixed64
                | FieldType::Sint32
                | FieldType::Sint64
                | FieldType::Enum
        )
    )
}

fn is_map_entry(msg: &DescriptorProto) -> bool {
    msg.options
        .as_ref()
        .and_then(|o| o.map_entry)
        .unwrap_or(false)
}

/// If this field is a map field, return (key_field, value_field) from the map entry type.
fn find_map_entry<'a>(
    field: &FieldDescriptorProto,
    parent_msg: &'a DescriptorProto,
) -> Option<(&'a FieldDescriptorProto, &'a FieldDescriptorProto)> {
    if field.label != Some(FieldLabel::Repeated) {
        return None;
    }
    if field.r#type != Some(FieldType::Message) {
        return None;
    }
    let type_name = field.type_name.as_deref()?;
    let short = type_name.rsplit('.').next()?;

    for nested in &parent_msg.nested_type {
        if nested.name.as_deref() == Some(short) && is_map_entry(nested) {
            let key = nested.field.iter().find(|f| f.number == Some(1))?;
            let val = nested.field.iter().find(|f| f.number == Some(2))?;
            return Some((key, val));
        }
    }
    None
}

