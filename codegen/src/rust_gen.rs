    #[test]
    fn test_integration_with_analyzer() {
        let proto = r#"
            syntax = "proto3";
            package example;

            enum Color {
                COLOR_UNKNOWN = 0;
                COLOR_RED = 1;
                COLOR_GREEN = 2;
                COLOR_BLUE = 3;
            }

            message Person {
                string name = 1;
                int32 id = 2;
                repeated string emails = 3;
                Color favorite_color = 4;
            }
        "#;

        let fds = protoc_rs_analyzer::analyze(proto).unwrap();
        let result = generate_rust(&fds).unwrap();
        let source = &result["example.rs"];

        // Check enum
        assert!(source.contains("pub enum Color"));
        assert!(source.contains("Unknown = 0,"));
        assert!(source.contains("Red = 1,"));

        // Check message
        assert!(source.contains("pub struct Person"));
        assert!(source.contains("pub name: ::prost::alloc::string::String,"));
        assert!(source.contains("pub id: i32,"));
        assert!(source.contains("pub emails: ::prost::alloc::vec::Vec<::prost::alloc::string::String>,"));
        assert!(source.contains("pub favorite_color: i32,"));
    }
